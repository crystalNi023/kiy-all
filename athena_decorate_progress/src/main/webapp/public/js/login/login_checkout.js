
$('#login_sub').click(function(){
	if($('#user_name').val()=="" || $('#user_name').val().length==0){
		$("#prompt_msg").text('请输入用户名');
		return false;
	}
	if($('#user_pwd').val()=="" || $('#user_pwd').val().length==0){
		$("#prompt_msg").text('请输入密码');
		return false;
	}
	$("#prompt_msg").text("");
})
